#include "libft.h"

/*Copies the first n characters of the array pointed to by src into the array 
pointed to by dest.
Returns a pointer to the destination.
*/

char 	*ft_strncpy(char *dest, const char *src, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}